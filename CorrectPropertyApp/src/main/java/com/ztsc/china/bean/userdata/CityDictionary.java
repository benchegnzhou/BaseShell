public class CityDictionary {
    //    NAME,ENAME,CODE,ADDRESS,X,Y
    private String cityName;
    private String cityENAME;
    private String cityCODE;
    private String cityADDRESS;
    private String cityX;
    private String cityY;

    public String getCityName() {
        return cityName;
    }

    public void setCityName(String cityName) {
        this.cityName = cityName;
    }

    public String getCityENAME() {
        return cityENAME;
    }

    public void setCityENAME(String cityENAME) {
        this.cityENAME = cityENAME;
    }

    public String getCityCODE() {
        return cityCODE;
    }

    public void setCityCODE(String cityCODE) {
        this.cityCODE = cityCODE;
    }

    public String getCityADDRESS() {
        return cityADDRESS;
    }

    public void setCityADDRESS(String cityADDRESS) {
        this.cityADDRESS = cityADDRESS;
    }

    public String getCityX() {
        return cityX;
    }

    public void setCityX(String cityX) {
        this.cityX = cityX;
    }

    public String getCityY() {
        return cityY;
    }

    public void setCityY(String cityY) {
        this.cityY = cityY;
    }


}
